# step function in for loop
for i in range(1,11,2):        # 1,11 showing the range; 2 is showing the steps, should jump
    print(i)
    #1 3 5 7 9 11

for j in range(12,0,-2):       # -2 for printing it reverse
    print(j)
    #10 8 6 4 2

 # it is useful to print odd or even numbers using for loop   