# for loop
#we use range function here
for i in range(10): # 0 to 9
    print(f"hii {i}") # range function decides the range of number
    #it will print 10 times
for i in range(1,10): # 1 to 9
    print(f"hii {i}") # range function decides the range of number
    #it will print 10 times
    